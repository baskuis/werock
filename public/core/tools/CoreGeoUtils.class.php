    /**
     * Attempt to lookup region
     *
     * @param null $region
     * @return null
     */
    public static function lookupRegion($region = null){
        $USRegions = self::getUSStateMap();
        if(isset($USRegions[strtoupper($region)])){
            return $USRegions[strtoupper($region)];
        }
        return $region;
